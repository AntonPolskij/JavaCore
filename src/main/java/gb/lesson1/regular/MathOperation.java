package gb.lesson1.regular;
/**Класс математических операций*/
public class MathOperation {
    /**Сложение
     * @param a,b слогаемые
     * @return сумма
     */
    public static int sum(int a, int b) {
        return a+b;
    }
    /**Вычитание
     * @param a уменьшаемое
     * @param b вычитаемое
     * @return разность
     */
    public static int diff(int a, int b) {
        return a-b;
    }
}
